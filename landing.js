// Show a specific modal by its ID
function showModal(modalId) {
    const modal = document.getElementById(modalId);
    if (modal) {
      modal.showModal();
    }
  }
  
  // Close a specific modal by its ID
  function closeModal(modalId) {
    const modal = document.getElementById(modalId);
    if (modal) {
      modal.close();
    }
  }




  